package ss.version1.main.Activity;

public class Activity {

	int id;
	String name;
	
	public Activity() {}
	
	public Activity(int id, String name) {
		super();
		this.id = id;
		this.name = name;
	}
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
}
